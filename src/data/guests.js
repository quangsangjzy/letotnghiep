export function getGuestFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const id = params.get("guest") || params.get("to") || "crossing";

  if (GUESTS[id]) {
    return { id, ...GUESTS[id] };
  }

  if (params.get("to")) {
    const name = params.get("to").trim();
    return {
      id: "custom",
      ...DEFAULT_GUEST,
      name,
      displayName: name,
      greeting: `Thân gửi ${name},`,
    };
  }

  return { id: "default", ...DEFAULT_GUEST };
}
